export interface DotSign {
  /** 7 行,每行是该行从左到右的「亮/灭」序列(含字间空列) */
  rows: boolean[][]
  /** 点与间距的像素尺寸 */
  dot: number
  gap: number
}

/** 把文字编译成点阵矩阵。dot 为单点像素边长。 */
export function buildSign(text: string, dot = 9): DotSign {
  const gap = Math.max(2, Math.round(dot / 3))
  const glyphs = [...String(text).toUpperCase()].map((ch) => FONT[ch] || FONT[' '])
  const rows: boolean[][] = []
  for (let r = 0; r < 7; r++) {
    const cells: boolean[] = []
    glyphs.forEach((g, gi) => {
      for (const ch of g[r]) cells.push(ch === '1')
      if (gi < glyphs.length - 1) cells.push(false) // 字间空列
    })
    rows.push(cells)
  }
  return { rows, dot, gap }
}
